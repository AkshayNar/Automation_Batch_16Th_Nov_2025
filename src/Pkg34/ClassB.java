public class ClassB {


    public static void main(String[] args) {

        //Explicit casting
        float a = 120.25f;
        int b = (int) a;
        System.out.println(b);


        int c = 130;
        byte d = (byte) c;     // -128 to +127
        System.out.println(d);


    }

}
